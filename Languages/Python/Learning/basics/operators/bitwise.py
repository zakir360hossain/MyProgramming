'''
Bitwise operations are performed with the binary version of the given numbers
'''



# Complement bitwise operator.
print(~12) #-13
print(~16)
print(~-12)


# AND Bitwise operator
print(14 & 17)

# OR Bitwise operator
print(12 | 13)

# XOR Operator
print(12^13)

# Left Shift Operator
print(10<<2)
print(13<<4)

# Right Shift
print(10>>1)
