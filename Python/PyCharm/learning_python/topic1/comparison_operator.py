# Integer
print(3 < 6)  # True
print(5 > 6)  # False

# String
print('Zack' < 'zack ')  # True because ASCII value for "Z" < "z"

# Boolean
print(0 > False)  # False, 0 > 0 is not true

# Tuple
print((3, 4, 5, 6) > (3, 3, 5, 6))  # True, 4 > 3

# Different data types
print((1, 2) > ('one', 'two'))  # Non-comparable. => error

# List
print([1, 2, 4] >= [1, 2, 4])  # True

